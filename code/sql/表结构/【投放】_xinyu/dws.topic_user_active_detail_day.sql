-- 【业务定位】
--   C 端用户日活跃明细主表之一
--
-- 【统计口径】
--   日活/学习活跃均需在默认 C 端筛选下计算（见【常用筛选条件】）
--
-- 【常用筛选条件】
--   ★必加（默认 C 端主产品移动端）：
--   - dt = '${date}' 或 dt 范围
--   - product_id = '01'
--   - client_os IN ('android', 'ios', 'harmony')
--   - active_user_attribution IN ('中学用户', '小学用户', 'c')
--
-- 【常用关联】
--   u_user 与 dim_user、topic_order_detail 等一致
